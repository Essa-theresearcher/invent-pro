function adjustStock(id) {
    const product = products.find(p => p.id === id);
    if (product) {
        modalTitle.textContent = 'Adjust Stock';
        modalBody.innerHTML = `
            <form id="stockForm">
                <div class="form-group">
                    <label>Product</label>
                    <input type="text" value="${product.name}" disabled>
                </div>
                <div class="form-group">
                    <label>Current Stock</label>
                    <input type="text" value="${product.stock}" disabled>
                </div>
                <div class="form-row">
                    <div class="form-group">
                        <label>Adjustment Type</label>
                        <select id="adjustmentType">
                            <option value="add">Add Stock</option>
                            <option value="remove">Remove Stock</option>
                            <option value="set">Set Stock</option>
                        </select>
                    </div>
                    <div class="form-group">
                        <label>Quantity</label>
                        <input type="number" id="adjustmentQty" value="0" min="0">
                    </div>
                </div>
                <div class="form-group">
                    <label>Reason</label>
                    <select id="adjustmentReason">
                        <option value="purchase">Purchase Order</option>
                        <option value="sale">Sale</option>
                        <option value="return">Customer Return</option>
                        <option value="damage">Damaged/Defective</option>
                        <option value="adjustment">General Adjustment</option>
                    </select>
                </div>
                <div class="modal-actions">
                    <button type="button" class="btn btn-outline" onclick="closeModal()">Cancel</button>
                    <button type="submit" class="btn btn-primary">
                        <i class="fas fa-save"></i> Save Adjustment
                    </button>
                </div>
            </form>
        `;

        openModal();

        document.getElementById('stockForm').addEventListener('submit', (e) => {
            e.preventDefault();
            const type = document.getElementById('adjustmentType').value;
            const qty = parseInt(document.getElementById('adjustmentQty').value);
            
            if (type === 'add') {
                product.stock += qty;
            } else if (type === 'remove') {
                product.stock = Math.max(0, product.stock - qty);
            } else {
                product.stock = qty;
            }

            // Update status
            if (product.stock === 0) {
                product.status = 'out-of-stock';
            } else if (product.stock < product.minStock) {
                product.status = 'low-stock';
            } else {
                product.status = 'in-stock';
            }

            closeModal();
            loadInventory();
            loadProducts();
            loadDashboard();
            showToast('success', 'Stock Adjusted', `${product.name} stock has been updated.`);
        });
    }
}

