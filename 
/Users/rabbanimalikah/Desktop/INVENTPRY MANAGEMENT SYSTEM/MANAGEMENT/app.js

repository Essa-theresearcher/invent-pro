document.getElementById('productSearch')?.addEventListener('input', function() {
    const query = this.value.toLowerCase();
    const filtered = products.filter(p => 
        p.name.toLowerCase().includes(query) || 
        p.sku.toLowerCase().includes(query) ||
        p.category.toLowerCase().includes(query)
    );
    
    const tbody = document.querySelector('#productsTable tbody');
    tbody.innerHTML = filtered.slice(0, 10).map(p => `
        <tr>
            <td><input type="checkbox"></td>
            <td>
                <div class="product-info">
                    <div class="product-img">
                        <i class="fas fa-box"></i>
                    </div>
                    <div>
                        <div class="product-name">${p.name}</div>
                        <div class="product-sku">${p.sku}</div>
                    </div>
                </div>
            </td>
            <td>${p.sku}</td>
            <td>${p.category}</td>
            <td>$${p.price.toFixed(2)}</td>
            <td>${p.stock}</td>
            <td><span class="status-badge ${p.status}">${formatStatus(p.status)}</span></td>
            <td>
                <div class="action-btns">
                    <button class="action-btn edit" title="Edit" onclick="editProduct(${p.id})">
                        <i class="fas fa-edit"></i>
                    </button>
                    <button class="action-btn delete" title="Delete" onclick="deleteProduct(${p.id})">
                        <i class="fas fa-trash"></i>
                    </button>
                </div>
            </td>
        </tr>
    `).join('');
});

// Settings navigation
document.querySelectorAll('.settings-nav-item').forEach(item => {
    item.addEventListener('click', (e) => {
        e.preventDefault();
        document.querySelectorAll('.settings-nav-item').forEach(i => i.classList.remove('active'));
        document.querySelectorAll('.settings-panel').forEach(p => p.classList.remove('active'));
        
        item.classList.add('active');
        const panelId = item.dataset.settings + 'Settings';
        document.getElementById(panelId)?.classList.add('active');
    });
});

