function formatStatus(status) {
    const statusMap = {
        'in-stock': 'In Stock',
        'low-stock': 'Low Stock',
        'out-of-stock': 'Out of Stock',
        'active': 'Active',
        'inactive': 'Inactive'
    };
    return statusMap[status] || status;
}

